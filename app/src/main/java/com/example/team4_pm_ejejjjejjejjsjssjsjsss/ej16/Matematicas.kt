class Matematicas {

    fun sumar(x : Float, y : Float) : Float{
        return x + y
    }

    fun restar(x : Float, y : Float) : Float{
        return x - y
    }

    fun multiplicar(x : Float, y : Float) : Float{
        return x * y
    }

    fun dividir(x : Float, y : Float) : Float{
        return x / y
    }
}